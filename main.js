var imagevisable = false;
function Visable(){
    var img = document.getElementById("imgcer");
    imagevisable = !imagevisable;

    if (imagevisable) {
        img.src="cer3.jpeg";
        img.style.display = "block";
    }
    else{
        img.style.display = "none";
    }
} 
